<h1 align="center">Geschichte der Drachen von Gaja</h1>
<p align="center" style="font-family:Lucida Calligraphy,Georgia,fantasy,EG Dragon Caps;font-size:18px;">
"Einst existierte der Lichtdrache <b>Sangha</b> als das reinste und gr&ouml;&szlig;te Seelenwesen.<br>
