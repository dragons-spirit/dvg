<h1 align="center">Vom Sternendrachen Sangha und Gaja</h1>
<p align="center">
"Einst existierte ein großer Sternendrache namens <b>Sangha</b> als das reinste und größte Seelenwesen und kosmische Bewusstein.<br />
Sangha war ein Drache, denn seine innere Energie der Liebe kann manchmal so heiß und zerstörerisch sein <br />
wie die Flammen eines wütenden Drachens. <br />
Eines Tages hatte Sangha den Wunsch nach einen Körper bekommen. <br />
Weil Sangha sich selbst erkennen und erfühlen wollte, teilte er seine Seele in zwei Hälften auf. <br />
Durch diese Teilung erschuff er einen männlichen Drachengott und eine weibliche Drachengöttin. <br />
Nun konnte Sangha sich selbst erkennen und erfühlen. <br />
Nach einer Weile bekamen der Drachengott und die Drachengöttin den Wunsch sich vereinen zu wollen. <br />
Durch diese Vereinigung wurde in der Drachengöttin ein neuer kleiner Drache gezeugt der im weiblichen Drachenschoß heranwuchs, <br />
ernährt von seiner eigenen Sonne. <br /> Der kleine Drache erhielt den Namen <b>Gaja</b>. <br />
Auf Gaja manifestierte sich Sanghas Liebe und Schöpfungskraft mit seiner Fantasie als alle möglichen Pflanzen und Tiere."</p>

<h1 align="center">Wer sind die Drachen von Gaja ?</h1>
<p align="center">
"Die Drachen von Gaja sind die Bewohner des Drachenkindes Gaja das sie für ihren Planeten halten da Gaja noch eine nahezu kugelige Form hat.<br /> Früher waren sie Menschen gewesen bis sie in einem neuen Leben<br />
als kleine Drachen aus Eiern geschlüpft waren. Als Drachen bekamen die Menschen die Chance die Welt aus einem anderen Blickwinkel<br />
kennen zu lernen und die Elemente zu beherrschen, die ihre Welt einst erschuf und formte."<br /><br />

