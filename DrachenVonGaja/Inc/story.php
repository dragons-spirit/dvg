<h1 align="center">Geschichte der Drachen von Gaja</h1>
<p align="center" style="font-family:Lucida Calligraphy,Georgia,fantasy,EG Dragon Caps;font-size:18px;">
Einst existierte der Lichtdrache <b>Sangha</b> als das reinste und gr&ouml;&szlig;te Seelenwesen.<br>
Der Lichtdrache manifestierte sich als die gro&szlig;e materielle See aus Lichtern (Weltall)<br>
in der au&szlig;erirdischen Welt und unendlich - fach, sowie paarweise (als m&auml;nnlich und weiblich)<br>
als winzige materielle Lebewesen auf der irdischen Welt. <br>
Alle Lichter der gro&szlig;en materiellen See sind seine Ab - und Spiegelbilder sowie auch alle Lebewesen seine Ab- und Spiegelbilder sind.<br>
Trotz der manigfaltigen Formen von Leben in die sich der Lichtdrache inkarnierte, beschloss er<br>
sich als ein Wesen zu manifestieren, dass die F&auml;higkeit zur Erinnerung besa&szlig;, falls er sein<br>
wahres Selbst je vergessen w&uuml;rde.<br>
So inkarnierte er sich als Dracheng&ouml;ttin die die Erde Gaja erschuff<br>
und als Drachengott der den Himmel &uuml;ber Gaja erschuff.<br>
Nach der Erschaffung von Erde Gaja und dem Himmel w&uuml;nschten sich beide Dracheng&ouml;tter<br>
die erschaffende Erde Gaja betreten und den erschaffenen Himmel befliegen zu k&ouml;nnen und so<br>
materialisierten sie sich in die Welt. <br>
Nachdem die zwei G&ouml;tter auf Gaja erschienen und sich materialisiert hatten,<br>
waren sie so stark ineinander verliebt, dass sie verga&szlig;en, dass sie Dracheng&ouml;tter waren und einst <br>
als der Lichtdrache Sangha die ganze Welt geschaffen hatten. <br>
Fortan waren sie das erste Drachenpaar auf Gaja welches die Welt von Gaja bev&ouml;lkerte.<br>
Leider wurden viele Generationen nach den zwei Dracheng&ouml;ttern<br>
durch das Vergessen ihrer wahren Herkunft primitiver, aggressiver und &auml;ngstlicher. <br>
Sie verloren ihre F&auml;higkeit zum Speien des Feuers und lernten wie man mit Holz und Stein Feuer entz&uuml;nden kann um sich zu w&auml;rmen,<br>
doch dieses Feuer wurde h&auml;ufig f&uuml;r Kriege verwendet.<br>
Sie verloren ihre F&auml;higkeit die Elemente der Natur zu erschaffen und zu formen und fingen an Elemente miteinander zu mischen,<br>
in der Hoffnung nach Arznei, doch sie erschuffen Gifte.<br>
Sie verloren ihre Fl&uuml;gel und versuchten fortan Maschinen zu bauen die sie in die Luft erheben konnte, <br>
doch diese Maschinen verschmutzen ihre Luft.<br>
<b>Die Drachen von Gaja wurden zu Menschen.</b> <br>
Kurz bevor die Menschen durch ihr zerst&ouml;rerisches Feuer, die schlechte Luft und die Gifte sich selbst und den Planeten Gaja<br>
beinahe zerst&ouml;rt hatten, brach die Erde unter ihnen auf und bestrafte die Menschen mit ihren elementaren Kr&auml;ften,<br>
durch Fluten (Wasser), Vulkanausbr&uuml;che (Feuer), Orkane (Luft) und Hungersnot (Erde). <br>
Viele Menschen kamen dabei ums Leben, aber die Menschen die sich wieder daran erinnerten wer sie wirklich waren, <br>
wurden wieder zu den Drachen von Gaja und als Eine/r von Ihnen musst <b>Du</b> wieder entdecken und neulernen deine wahren F&auml;higkeiten <br>
zu nutzen um den Planeten Gaja wieder heilen und besch&uuml;tzen zu k&ouml;nnen. </p>

