in der au&szlig;erirdischen Welt und unendlich - fach, sowie paarweise (als m&auml;nnlich und weiblich)<br>
als winzige materielle Lebewesen auf der irdischen Welt. <br>
Alle Lichter der gro&szlig;en materiellen See sind seine Ab - und Spiegelbilder sowie auch alle Lebewesen seine Ab- und Spiegelbilder sind.<br>
Trotz der manigfaltigen Formen von Leben in die sich der Lichtdrache inkarnierte, beschloss er<br>
sich als ein Wesen zu manifestieren, dass die F&auml;higkeit zur Erinnerung besa&szlig;, falls er sein<br>
wahres Selbst je vergessen w&uuml;rde.<br>
So inkarnierte er sich als Dracheng&ouml;ttin die die Erde Gaja erschuff<br>
und als Drachengott der den Himmel &uuml;ber Gaja erschuff.<br>
Nach der Erschaffung von Erde Gaja und dem Himmel w&uuml;nschten sich beide Dracheng&ouml;tter<br>
